public class LanguagesSkills {

    public String Languages; // 语种

    public String ListeningSpeakingSkills; // 听说能力

    public String ReadingWritingSkills; // 读写能力

    public String Score; // 分数

    public String Skills; // 程度

    public String getLanguages() {
        return Languages;
    }

    public void setLanguages(String languages) {
        Languages = languages;
    }

    public String getListeningSpeakingSkills() {
        return ListeningSpeakingSkills;
    }

    public void setListeningSpeakingSkills(String listeningSpeakingSkills) {
        ListeningSpeakingSkills = listeningSpeakingSkills;
    }

    public String getReadingWritingSkills() {
        return ReadingWritingSkills;
    }

    public void setReadingWritingSkills(String readingWritingSkills) {
        ReadingWritingSkills = readingWritingSkills;
    }

    public String getScore() {
        return Score;
    }

    public void setScore(String score) {
        Score = score;
    }

    public String getSkills() {
        return Skills;
    }

    public void setSkills(String skills) {
        Skills = skills;
    }
}
